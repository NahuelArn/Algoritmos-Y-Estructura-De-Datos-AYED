package ResumenAlgoritmos.Practica3.eje4;

public class AreaEmpresa {
	private String id;
	private int delay;

	public AreaEmpresa(String sarasa, int d) {
		this.id = sarasa;
		this.delay = d;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getDelay() {
		return delay;
	}

	public void setDelay(int delay) {
		this.delay = delay;
	}

}
